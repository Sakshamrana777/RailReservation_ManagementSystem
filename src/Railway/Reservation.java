    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String pid = jComboBox3.getSelectedItem().toString();
        String splace = jComboBox1.getSelectedItem().toString();
        String dplace = jComboBox2.getSelectedItem().toString();
        String tno =jTextField2.getText();
        String tname =jTextField3.getText();
        SimpleDateFormat date_form = new SimpleDateFormat("yyyy-mm-dd");
        String date = date_form.format(jDateChooser1.getDate());
        String price =jTextField6.getText();
        String tickets =jTextField4.getText();
        String total =jTextField5.getText();
       
        
        
        try {
           
            pst = con.prepareStatement("insert into reservation(pid,Splace,Destination,tno,tname,Date,price,ticket,total)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1,pid);
            pst.setString(2,splace);
            pst.setString(3,dplace);
            pst.setString(4,tno);
            pst.setString(5,tname);
            pst.setString(6 ,date);
            pst.setString(7,price);
            pst.setString(8,tickets);
            pst.setString(9,total);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Booked Successfully");
            }
            else{
                JOptionPane.showMessageDialog(this,"Record Failed");
            }    
                
            jComboBox1.setSelectedItem(null);
            jComboBox2.setSelectedItem(null);
            jComboBox3.setSelectedItem(null);
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            
            jComboBox1.requestFocus();
            
                
  
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

