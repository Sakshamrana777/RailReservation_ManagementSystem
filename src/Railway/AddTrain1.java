    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(jTextField1.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter Train No");
       }
       else if(jTextField2.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter Train Name");
       }
       else if(jTextField3.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter Train Start Place");
       }
       else if(jTextField4.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter Train Destination");
       }
       else if(jTextField5.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter Fare");
       }
       else{
       
       
        String tno = jTextField1.getText();
        String tname = jTextField2.getText();
        String splace = jTextField3.getText();
        String dplace = jTextField4.getText();
        String price = jTextField5.getText();
        
        
        try {
           
            pst = con.prepareStatement("insert into train_details(tno,tname,start,destination,price)values(?,?,?,?,?)");
            pst.setString(1,tno);
            pst.setString(2,tname);
            pst.setString(3,splace);
            pst.setString(4,dplace);
            pst.setString(5,price);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this, "Record Added !!");
            }
            else{
                JOptionPane.showMessageDialog(this,"Record Failed");
            }    
                
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            
            jTextField1.requestFocus();
            
                
  
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain1.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
       
    }//GEN-LAST:event_jButton1ActionPerformed

