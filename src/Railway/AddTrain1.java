    public void table(){
        try {
            pst=con.prepareStatement("select * from train_details");
            rs = pst.executeQuery();    
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            
            while(rs.next()){
               
               Vector v = new Vector();
               for(int i=0; i<=c; i++){
                   v.add(rs.getString("tno"));
                   v.add(rs.getString("tname"));
                   v.add(rs.getString("start"));
                   v.add(rs.getString("destination"));
                   v.add(rs.getString("price"));
                   
               }
               d.addRow(v);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddTrain1.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }

